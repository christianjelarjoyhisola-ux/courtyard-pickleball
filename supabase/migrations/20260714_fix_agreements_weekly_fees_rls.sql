-- Close fresh-project RLS gaps for electronic agreements and weekly billing.
-- The legacy `admin` role is named `court_owner` in the current role model.

-- Historical receipt policies used `bucket_id <> 'receipts'`. Because RLS
-- policies are permissive, those predicates opened every non-receipts bucket
-- to anon/authenticated. Direct receipt access is default-deny; the Edge
-- Function uses service_role and does not need a storage.objects policy.
drop policy if exists receipts_no_select on storage.objects;
drop policy if exists receipts_no_insert on storage.objects;
drop policy if exists receipts_no_update on storage.objects;
drop policy if exists receipts_no_delete on storage.objects;

-- --------------------------------------------------------------------------
-- Agreements
-- --------------------------------------------------------------------------

alter table public.agreements enable row level security;

drop policy if exists "users_read_own_agreement" on public.agreements;
drop policy if exists agreements_select_own_or_admin on public.agreements;
drop policy if exists agreements_insert_own_or_admin on public.agreements;
drop policy if exists agreements_update_own_or_admin on public.agreements;
drop policy if exists agreements_delete_admin on public.agreements;

-- Every dashboard role can read only its own acceptance. The system owner is
-- the only role with global agreement oversight because agreement rows contain
-- legal-name, signature, IP, and user-agent data.
create policy agreements_select_own_or_admin
on public.agreements
for select
to authenticated
using (
  (
    user_id = auth.uid()::text
    and public.current_dashboard_role() in ('owner', 'court_owner', 'staff')
  )
  or public.current_dashboard_role() = 'owner'
);

-- This permits the client's ON CONFLICT (user_id, version) upsert while
-- preventing an ordinary authenticated account from accepting for anyone else.
create policy agreements_insert_own_or_admin
on public.agreements
for insert
to authenticated
with check (
  (
    user_id = auth.uid()::text
    and public.current_dashboard_role() in ('owner', 'court_owner', 'staff')
  )
  or public.current_dashboard_role() = 'owner'
);

create policy agreements_update_own_or_admin
on public.agreements
for update
to authenticated
using (
  (
    user_id = auth.uid()::text
    and public.current_dashboard_role() in ('owner', 'court_owner', 'staff')
  )
  or public.current_dashboard_role() = 'owner'
)
with check (
  (
    user_id = auth.uid()::text
    and public.current_dashboard_role() in ('owner', 'court_owner', 'staff')
  )
  or public.current_dashboard_role() = 'owner'
);

create policy agreements_delete_admin
on public.agreements
for delete
to authenticated
using (public.current_dashboard_role() = 'owner');

